/** Loose match: snapshot query contained in the asked query or vice versa. */
export function findSnapshot(
  mode: "timelinetone" | "timelinevolraw",
  query: string,
): GdeltSnapshot | undefined {
  const q = query.trim().toLowerCase();
  return GDELT_SNAPSHOTS.find(
    (s) => s.mode === mode && (q.includes(s.query) || s.query.includes(q)),
  );
}
